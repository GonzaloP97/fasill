		<h4><i class="fab fa-linux"></i> Install on Linux</h4>
		<ol>
			<li><p><a href="https://github.com/jariazavalverde/fasill/archive/master.zip">Download</a> or clone the FASILL repository: <kbd>git clone https://github.com/jariazavalverde/fasill.git</kbd></p></li>
			<li><p>Enter the <mark>fasill</mark> folder: <kbd>cd fasill</kbd></p></li>
			<li><p>Execute the <mark>install.sh</mark> bash script: <kbd>sudo ./install.sh</kbd></p></li>
			<li><p>That's all! Now you can run FASILL by typing <mark>fasill</mark> in your terminal: <kbd>fasill</kbd></p></li>
